
public class basic {
    public static void main(String[] args) {
        System.out.println("Hello, World!"); // println automatically adds new line after printing

        float x= 7.8f;
        int y=5;
        double z=5.6;
        double sum = y+z+x;
        System.out.print("result = "); // print will not add any line after printing
        System.out.println(sum);

        boolean a=true;
        System.out.println(a);

        char s='f';
        System.out.println(s);

        String str="Hey there";
        System.out.println(str);
    }
}